"""
config.py — единственное место со всеми «магическими числами» проекта.

Зачем так: когда захочется подкрутить, какой именно красный считать «разломом»
или насколько сильно чистить шум, ты меняешь число ЗДЕСЬ, а не ищешь его по всему коду.
Это же помогает на скрытом датасете судьи: достаточно поправить пороги в одном файле.

Важно про HSV в OpenCV: диапазоны НЕ как в школе!
  H (тон):        0..179   (а не 0..360)
  S (насыщенность): 0..255
  V (яркость):      0..255
"""

# ----------------------------------------------------------------------------
# 1. Предобработка (этап 2)
# ----------------------------------------------------------------------------

# Если скан огромный (например, 6000px по длинной стороне) — ужимаем,
# чтобы обработка шла быстрее и было меньше шума. 0 = не менять размер.
MAX_IMAGE_SIDE = 2000  # пикселей по длинной стороне

# Обрезка до рамки карты (этап после ресайза). По умолчанию ВЫКЛ: на мятых
# исторических картах прямоугольная рамка детектится ненадёжно (может отрезать угол).
# Это опциональная фича — включается установкой True. Безопасно: если рамка не найдена,
# кадр не режется. Доли площади ограничивают, какой контур считать «рамкой карты».
CROP_TO_MAP_BORDER = False
CROP_MIN_AREA_FRAC = 0.30   # рамка должна занимать не меньше 30% кадра
CROP_MAX_AREA_FRAC = 0.95   # и не больше 95% (иначе это весь лист, а не рамка)

# Маскировать всё ЗА пределами найденной рамки карты (поля, легенду, штамп).
# Безопасно: если рамка не найдена — ничего не маскируем. В отличие от обрезки,
# координаты не сдвигаются (мы только зануляем маски снаружи рамки), поэтому
# геопривязка и debug-кадры остаются согласованными. Помогает от ложных срабатываний
# на цветных образцах легенды и пометках на полях.
MASK_OUTSIDE_FRAME = True

# CLAHE — «умный» контраст по участкам (помогает с выцветанием).
CLAHE_CLIP_LIMIT = 2.0
CLAHE_TILE_GRID = (8, 8)

# Сила денойза (чем больше — тем сильнее сглаживание зерна бумаги/карандаша).
DENOISE_STRENGTH = 5  # нечётное число для медианного фильтра, 0 = выключить


# ----------------------------------------------------------------------------
# 2. Цветовые профили для выделения объектов (этап 3) — СЕРДЦЕ настройки
# ----------------------------------------------------------------------------
#
# Профиль = словарь «название цвета -> список HSV-диапазонов».
# Каждый диапазон это (нижняя граница HSV, верхняя граница HSV).
# Для красного нужны ДВА диапазона, потому что красный тон «разорван»
# на концах круга H (около 0 и около 179) — поэтому ловим оба и объединяем.
#
# Тип каждого объекта (fault/boundary) попадёт в свойства GeoJSON.

PROFILES = {
    # Профиль для цветных геологических карт.
    "geological": {
        "red": {
            "type": "fault",          # красные линии трактуем как разломы
            "ranges": [
                ((0, 70, 50),   (10, 255, 255)),    # красный у начала круга
                ((170, 70, 50), (179, 255, 255)),   # красный у конца круга
            ],
        },
        "green": {
            "type": "boundary",       # зелёные линии/дайки — границы слоёв
            # Пороги по реальным замерам: оливковые дайки тусклые (H~35-40, S~35+, V~90+),
            # поэтому S/V опущены. H начинается с 33 — выше тона фона бумаги (H~15),
            # так что фон по тону НЕ попадает.
            "ranges": [
                ((33, 30, 30), (95, 255, 220)),
            ],
        },
        "blue": {
            "type": "boundary",
            "ranges": [
                ((90, 30, 30), (140, 255, 220)),
            ],
        },
    },

    # Профиль для калек/серых чертежей: цвета нет, опираемся на края (Canny).
    # Пустой словарь цветов = пайплайн пойдёт только по краям.
    "pencil": {},
}

# Профиль по умолчанию, если в командной строке не указали --profile.
DEFAULT_PROFILE = "geological"


# ----------------------------------------------------------------------------
# 3. Поиск краёв (этап 3, ветка для карт без цвета)
# ----------------------------------------------------------------------------

# Использовать ли Canny дополнительно к цвету. Для калек это единственный путь.
USE_CANNY = True
CANNY_THRESHOLD_LOW = 50
CANNY_THRESHOLD_HIGH = 150


# ----------------------------------------------------------------------------
# 3b. Тёмные линии (этап 3) — разломы/линеаменты, нарисованные ТЁМНЫМИ чернилами
# ----------------------------------------------------------------------------
#
# На многих советских картах разломы и геологические линии — это тёмные штрихи,
# а не цветные. HSV их не видит. Выделяем их «чёрной шляпой» (black-hat): операция
# подсвечивает тёмные тонкие структуры на светлом фоне. Затем порог -> бинарная маска.
# Тип таких линий — 'fault_uncertain' (тёмная линия = вероятный разлом, но без цвета,
# поэтому помечаем как менее уверенный).
EXTRACT_DARK_LINES = True
DARK_BLACKHAT_KERNEL = 15        # размер ядра black-hat (нечётный): ширина «ловимой» линии
DARK_THRESHOLD = 30              # порог яркости black-hat (0..255): больше = строже
# Фильтр формы для тёмных компонент: оставляем только ДЛИННЫЕ и ТОНКИЕ (линии),
# выкидываем короткие кляксы и толстые пятна (буквы, штриховку рельефа, заливки).
DARK_MIN_LENGTH = 60             # мин. длинная сторона bbox компоненты, пикс.
DARK_MAX_THICKNESS = 6.0         # макс. средняя толщина = area / длинная_сторона, пикс.


# ----------------------------------------------------------------------------
# 4. Очистка маски (этап 4)
# ----------------------------------------------------------------------------

# Размер «кисти» морфологии: чем больше, тем агрессивнее убираем шум/соединяем разрывы.
MORPH_KERNEL_SIZE = 3            # пикселей (нечётное)
MORPH_OPEN_ITERATIONS = 1       # убрать одиночные точки-шум
MORPH_CLOSE_ITERATIONS = 2      # соединить мелкие разрывы линии

# Выкидываем «кляксы» меньше этого числа пикселей (буквы, крошки, пятна).
MIN_COMPONENT_AREA = 80

# Гард от краевых пятен и рамки: убираем связные компоненты, КАСАЮЩИЕСЯ края кадра.
# Зачем: реальные разломы и границы лежат внутри рамки карты (neat-line) и не доходят
# до самого края листа. А вот коричнево-рыжие пятна старения, тень/линия рамки и поля
# касаются края — и их красный оттенок ложно ловится как «разлом» (особенно на сепия-
# сканах). Срезаем главный источник ложных срабатываний. Если рамка карты найдена и
# включён MASK_OUTSIDE_FRAME — этот фильтр и так не сработает (внутри рамки до края
# ничего не касается), поэтому два механизма не конфликтуют, а дополняют друг друга.
DROP_BORDER_TOUCHING = True
BORDER_TOUCH_TOLERANCE_FRAC = 0.012   # «касание» = компонента в пределах 1.2% от края

# «Мост» для линий разломов: дополнительный CLOSE с бОльшим ядром, чтобы соединить
# разорванные штрихи одной линии в цельный сегмент. Применяется ТОЛЬКО к маскам типа
# fault (разломы), и ДО фильтра мелочи — тогда сшитые штрихи переживают фильтр.
BRIDGE_KERNEL_SIZE = 7      # пикселей (нечётное); 0 = выключить мост
BRIDGE_ITERATIONS = 1


# ----------------------------------------------------------------------------
# 5. Векторизация (этап 5)
# ----------------------------------------------------------------------------

# Douglas-Peucker: насколько упрощать ломаную (больше = меньше точек, грубее).
APPROX_EPSILON = 2.0            # пикселей

# Контуры короче этого (в точках) выбрасываем как мусор.
MIN_CONTOUR_POINTS = 5

# Осевые линии через скелетизацию: вместо обводки пятна контуром-«петлёй» (точки
# задваиваются) утончаем маску до линии в 1px и трассируем её как полилинию.
# Требует scikit-image; если его нет — автоматический фолбэк на контуры.
USE_SKELETON = True
# Скелетные сегменты короче этого (в пикселях суммарной длины) считаем шумом.
MIN_SKELETON_LENGTH = 15


# ----------------------------------------------------------------------------
# 6. Триаж / уверенность (этап 6)
# ----------------------------------------------------------------------------

# Если цветных пикселей в маске меньше этой доли от всей картинки —
# считаем, что цвет «не сыграл» (вероятно калька) и помечаем low_confidence.
LOW_CONFIDENCE_COVERAGE = 0.001   # 0.1% площади

# Триаж по краевому шуму: если бОльшая часть найденных объектов жмётся к краю кадра —
# это, скорее всего, пятна старения/рамка, а не геология. Помечаем карту low_confidence,
# даже если объектов много (защита от обманчиво большого счётчика на сепия-сканах).
EDGE_NOISE_FRAC = 0.5     # доля «краевых» объектов, выше которой понижаем уверенность
EDGE_BAND_FRAC = 0.06     # «краевой полосой» считаем 6% от размера по каждой стороне


# ----------------------------------------------------------------------------
# 6b. Геопривязка (etап после векторизации) — Трек 2 «привязка карт»
# ----------------------------------------------------------------------------
#
# Исходный CRS координат AOI по умолчанию. Совет ментора: советские карты — это
# Пулково 1942 (EPSG:4284), а НЕ WGS84. Если AOI задан в WGS84 — укажите EPSG:4326
# в самом файле AOI (поле crs / строка '# epsg=4326'), и переход станет тождественным.
DEFAULT_SOURCE_EPSG = 4284   # Пулково 1942
TARGET_EPSG = 4326           # WGS84


# ----------------------------------------------------------------------------
# 6c. Извлечение легенды (Трек 2: «извлечение легенды»)
# ----------------------------------------------------------------------------
#
# Легенда карты — это таблица «образец цвета -> название геологического слоя».
# Мы не просто отсекаем легенду как шум: отдельным проходом находим в ней цветные
# ОБРАЗЦЫ (плотные прямоугольные заливки, а не тонкие линии) и связываем каждый
# образец с типом объекта (fault/boundary) и с уже извлечёнными векторами того же
# цвета. Результат пишем в <карта>.legend.json и кладём сводку в метаданные GeoJSON.
# OCR подписей слоёв (кириллица) — задача Трека 1; здесь намеренно без OCR, чтобы
# не тянуть Tesseract и остаться воспроизводимым «из коробки».
EXTRACT_LEGEND = True
# Размер образца легенды как доля площади кадра: не пылинка и не пол-карты.
LEGEND_MIN_SWATCH_AREA_FRAC = 0.00015
LEGEND_MAX_SWATCH_AREA_FRAC = 0.05
# Образец легенды — ПЛОТНАЯ заливка: площадь / площадь_bbox высокая. Линии разломов
# тонкие и извилистые, у них это отношение низкое, поэтому они сюда не попадают.
LEGEND_MIN_FILL_RATIO = 0.55
# И примерно компактный (не сильно вытянутый), чтобы отсечь куски линий.
LEGEND_ASPECT_MIN = 0.25
LEGEND_ASPECT_MAX = 4.0
# Образцы легенды обычно ВНЕ рамки карты (на полях) — мы пишем признак outside_frame
# в каждый образец. Но делать это ЖЁСТКИМ фильтром нельзя: детектор рамки часто
# хватает внешний край листа целиком, и тогда легенда формально «внутри». Поэтому
# по умолчанию НЕ требуем «вне рамки», а полагаемся на форму (плотная компактная
# заливка vs тонкая линия). Включите True, если на вашем датасете рамка стабильно
# совпадает с neat-line и легенда всегда снаружи.
LEGEND_REQUIRE_OUTSIDE_FRAME = False


# ----------------------------------------------------------------------------
# 6d. SAM-режим (опциональный, под флагом --use-sam) — критерий «инновационность»
# ----------------------------------------------------------------------------
#
# Segment Anything Model (совет ментора) как АЛЬТЕРНАТИВА цветовому порогу для
# карт со сложными границами слоёв. Включается ТОЛЬКО флагом --use-sam и тянет
# тяжёлые зависимости (torch + segment-anything + чекпойнт) из requirements-sam.txt.
# По умолчанию ВЫКЛ: дефолтный конвейер остаётся лёгким и воспроизводимым «из коробки».
# Если SAM/torch/чекпойнт недоступны — печатаем подсказку и тихо откатываемся на
# классический HSV-путь (конвейер не падает).
USE_SAM = False
SAM_MODEL_TYPE = "vit_b"     # vit_b (лёгкий) / vit_l / vit_h (точнее, тяжелее)
