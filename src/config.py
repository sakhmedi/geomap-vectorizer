SAM_CHECKPOINT = "models/sam_vit_b_01ec64.pth"
# Берём только вытянутые сегменты SAM (границы/линеаменты), отсекая крупные заливки.
SAM_MIN_REGION_AREA = 200
SAM_MAX_REGION_AREA_FRAC = 0.25


